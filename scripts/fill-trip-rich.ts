/**
 * เติมฟิลด์รายละเอียดทริป + โปรไฟล์ผู้จัดให้ครบ (สำหรับ dev / demo)
 * ใช้: npx tsx scripts/fill-trip-rich.ts [tripId]
 */
import { PrismaClient } from "@prisma/client";
import { assignUniqueShareCodeForTrip } from "../src/lib/trip-share-code";

const prisma = new PrismaClient();

const TRIP_ID = process.argv[2] ?? "cmo590blo0003xcmfhww9f16q";

const COVER =
  "https://images.unsplash.com/photo-1464822759023-fed622ff2c3b?w=1200&h=600&fit=crop";
const GALLERY = [
  "https://images.unsplash.com/photo-1506905925346-21bda4d32df4?w=800&h=800&fit=crop",
  "https://images.unsplash.com/photo-1478131143081-80f7f84caae0?w=800&h=800&fit=crop",
  "https://images.unsplash.com/photo-1511593358241-7eea1f3dc84b?w=800&h=800&fit=crop",
].join("\n");

const ORG_AVATAR =
  "https://images.unsplash.com/photo-1507003211169-0a1dd7228f2d?w=200&h=200&fit=crop";

async function main() {
  const trip = await prisma.trip.findUnique({
    where: { id: TRIP_ID },
    select: { id: true, title: true, organizerId: true },
  });

  if (!trip) {
    console.error(`ไม่พบทริป id=${TRIP_ID}`);
    process.exit(1);
  }

  await prisma.trip.update({
    where: { id: TRIP_ID },
    data: {
      shortDescription:
        "เดินป่าเชิงธรรมชาติ 1 วัน — เหมาะผู้มีพื้นฐานเดินป่าเล็กน้อย เน้นความปลอดภัยและจุดชมวิว",
      description: `ทริป "${trip.title}" ออกแบบให้สบายและชัดเจนเรื่องเวลา
เริ่มเช้าเพื่อเลี่ยงฝนบ่าย มีจุดพักและน้ำดื่มตลอดเส้นทาง
ไกด์อธิบายธรรมชาติรอบตัวแบบเบาๆ ไม่เร่งรัด

สิ่งที่คุณจะได้สัมผัส: อากาศบริสุทธิ์ เสียงป่า และมุมมองที่เปลี่ยนไปตามแสงในแต่ละช่วงเวลา`,
      meetPoint:
        "ลานจอดรถหน้าป้ายอุทยาน ฝั่งทิศเหนือ — รวมกลุ่ม 05:45 น. (ตรงเวลาเพื่อออกเดินทาง 06:00 น.)",
      coverImageUrl: COVER,
      galleryImageUrls: GALLERY,
      guideDetails: `ไกด์หลัก 1 ท่าน + ผู้ช่วย 1 ท่าน
• ไกด์หลัก: ใบอนุญาตนำเที่ยวภูเขา / ประสบการณ์นำกลุ่มเดินป่า 5+ ปี
• ผู้ช่วย: ดูแลท้ายแถว ช่วยจุดพัก และประสานงานฉุกเฉิน
• ก่อนออกเดินทางมีบรีฟเส้นทาง ระยะทางโดยประมาณ และจุดที่ต้องระวัง`,
      itinerary: `05:45 — รวมกลุ่ม เช็กชื่อ บรีฟเส้นทาง
06:00 — ออกเดินจากจุดนัด
08:30 — พักดื่มน้ำ ถ่ายรูปจุดแรก
10:30 — มื้อเบา (อาหารว่างจากไกด์)
12:30 — ถึงจุดชมวิวหลัก พักยาว
14:30 — เดินกลับเส้นทางเดิม / ทางลัดตามสภาพกลุ่ม
16:00 — ถึงจุดนัดพบ แยกย้าย`,
      travelNotes: `• รถส่วนตัว: จอดลานหน้าอุทยาน (ค่าบริการตามประกาศหน่วยงาน)
• รถตู้ร่วม: แจ้งผู้จัดล่วงหน้าเพื่อจองที่นั่ง (คิดค่าบริการแยก)
• หากใช้รถไฟ/รถประจำทาง: แนะนำถึงสถานี/ป้ายใกล้จุดนัดก่อน 05:30 น.`,
      highlights: `• จุดชมวิวกลางเส้นทาง — มุมมองกว้างเห็นแนวเขต่อเนื่อง
• ช่วงเช้า: แสงนุ่ม เหมาะถ่ายภาพ
• โอกาสพบนกท้องถิ่นและพืชสวยงามตามฤดูกาล
• จุดหมายปลายทางช่วงกลางวัน: พื้นที่โล่งลมแรง — รู้สึกถึงขนาดของธรรมชาติ`,
      packingList: `• น้ำดื่ม 1.5–2 ลิตร / คน
• รองเท้าเดินป่าที่ใส่แล้วไม่ปวดเท้า
• หมวก แว่นกันแดด ครีมกันแดด
• แจ็กเก็ตบางหรือ Windbreaker
• ถุงมือบาง (ถ้าต้องโยนตัวจับเชือก)
• ถุงขยะเล็กสำหรับห่อขยะกลับ`,
      safetyNotes: `• ช่วงฝน: หินลื่น — ลดความเร็ว ใช้ไม้เท้าถ้ามี
• ป่าทึบ: ระวังทาก — แนะนำถุงเท้ายาว + สเปรย์กันยุง
• ฟ้าร้องใกล้: ไกด์มีอำนาจเปลี่ยนเส้นทางหรือยกเลิกช่วงเสี่ยง
• แจ้งโรคประจำตัว / ยาที่ต้องทานประจำก่อนร่วมทริป`,
      guideProvides: `• อาหารว่าง 1 มื้อตามที่ระบุในทริป
• น้ำดื่มกลางวัน (รีฟิลจุดพัก)
• ปฐมพยาบาลเบื้องต้น + แผนที่เส้นทาง
• ไม้เท้าให้ยืม (จำนวนจำกัด — แจ้งล่วงหน้า)`,
      policyNotes: `• ยกเลิกก่อนชำระเงิน: แจ้งผ่านระบบได้ตามเงื่อนไขทริป
• หลังชำระเงิน: การคืนเงินขึ้นกับนโยบายผู้จัดและช่วงเวลา — โปรดอ่านก่อนจอง
• หากสภาพอากาศรุนแรง ผู้จัดอาจเลื่อนหรือปรับโปรแกรมเพื่อความปลอดภัย`,
    },
  });

  await prisma.user.update({
    where: { id: trip.organizerId },
    data: {
      bio: "ผู้จัดและไกด์เดินป่า — เน้นกลุ่มเล็ก ความปลอดภัย และประสบการณ์ที่จดจำได้",
      avatarUrl: ORG_AVATAR,
    },
  });

  await assignUniqueShareCodeForTrip(TRIP_ID);

  console.log(`อัปเดตทริป + โปรไฟล์ผู้จัดเรียบร้อย: ${TRIP_ID}`);
}

main()
  .then(() => prisma.$disconnect())
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
